/* ---------------------------------------------------------------------------
 * タイトル：スキーマーを削除する
 * 概    要：スキーマーを削除し、データベース内を空にする
 * ---------------------------------------------------------------------------
 */

-- 「private」スキーマーを削除し、再作成する
DROP SCHEMA IF EXISTS private CASCADE;

-- 「public」スキーマーを削除し、再作成する
DROP SCHEMA IF EXISTS public CASCADE;
CREATE SCHEMA public;

